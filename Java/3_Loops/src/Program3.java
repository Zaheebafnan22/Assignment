//3. Program to equal operator and not equal operators

public class Program3 {

	public static void main(String[] args) {
		
		//creating a value for a and b to perform equal or not equal operator
		int a = 5;
		int b = 5;
				
		//Equal operator - if both the values are equal
		if(a==b) {
			System.out.println("The value of a and b are equal");
		}
				
		//Not equal operator  if both the values are not equal
		else if(a != b){
			System.out.println("The value of a and b are not equal");
		}

	}

}
