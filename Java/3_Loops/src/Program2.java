//2. Write a java program to print 1 to 20 numbers using the while loop.

public class Program2 {

	public static void main(String[] args) {
		
		//Initializing
		int i = 1;
		
		//checking the condition
		while(i<=20) {
			
			//printing the number 1 to 20
			System.out.println(i);
			
			//incrementing
			i++;
		}

	}

}
