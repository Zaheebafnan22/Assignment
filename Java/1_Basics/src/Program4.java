//4. Define variables for different Data Types int, Boolean, char, float, double and print on the Console

public class Program4 {

	public static void main(String[] args) {
		
		//This is an integer this is 4 byte in size
		int i = 150;
		
		//This is a float this is 4 byte in size
		float f = 48.5f;
		
		//This is a double this is 8 byte in size
		double d = 83.65;
		
		//This is a character this is 2 byte in size
		char c ='C';
		
		//THis is a boolean this is 1 byte in size
		boolean b = true;
		
		System.out.println("This is an integer : "+i);
		System.out.println("This is a float : "+f);
		System.out.println("This is a double : "+d);
		System.out.println("This is a character : "+c);
		System.out.println("This is a boolean : "+b);
		

	}

}
